public class Time {
    private int min;
    private int sec;
    public void setMin(int min)
    {
        this.min=min;
    }
    public void setSec(int sec)
    {
        this.sec=sec;
    }
    public int getMin()
    {
        return this.min;
    }
    public int getSec()
    {
        return this.sec;
    }
}
